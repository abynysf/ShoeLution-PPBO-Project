public class SepatuModel {
    private String idSepatu;
    private String nameMerk;
    private String namaSepatu;
    private String jenisSepatu;
    private String warna;
    private Integer ukuran;
    private Double harga;
    private Integer jumlahStok;

    public String getIdSepatu() {
        return idSepatu;
    }

    public void setIdSepatu(String idSepatu) {
        this.idSepatu = idSepatu;
    }

    public String getNameMerk() {
        return nameMerk;
    }

    public void setNameMerk(String nameMerk) {
        this.nameMerk = nameMerk;
    }

    public String getNamaSepatu() {
        return namaSepatu;
    }

    public void setNamaSepatu(String namaSepatu) {
        this.namaSepatu = namaSepatu;
    }

    public String getJenisSepatu() {
        return jenisSepatu;
    }

    public void setJenisSepatu(String jenisSepatu) {
        this.jenisSepatu = jenisSepatu;
    }

    public String getWarna() {
        return warna;
    }

    public void setWarna(String warna) {
        this.warna = warna;
    }

    public Integer getUkuran() {
        return ukuran;
    }

    public void setUkuran(Integer ukuran) {
        this.ukuran = ukuran;
    }

    public Double getHarga() {
        return harga;
    }

    public void setHarga(Double harga) {
        this.harga = harga;
    }

    public Integer getJumlahStok() {
        return jumlahStok;
    }

    public void setJumlahStok(Integer jumlahStok) {
        this.jumlahStok = jumlahStok;
    }

    
}
